// مكون FAQ Schema للـ SEO
// يضيف JSON-LD structured data لأسئلة FAQ

export interface FAQItem {
  question: string
  answer: string
}

export interface FAQSchemaProps {
  faqs: FAQItem[]
  pageUrl?: string
}

export function generateFAQSchema(faqs: FAQItem[]): object {
  return {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    mainEntity: faqs.map(faq => ({
      '@type': 'Question',
      name: faq.question,
      acceptedAnswer: {
        '@type': 'Answer',
        text: faq.answer
      }
    }))
  }
}

export function FAQSchemaScript({ faqs }: FAQSchemaProps) {
  const schema = generateFAQSchema(faqs)
  
  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{ __html: JSON.stringify(schema) }}
    />
  )
}

